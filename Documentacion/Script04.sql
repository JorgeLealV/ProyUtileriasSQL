-- ==========================================================
-- 8. ÍNDICES DE OPTIMIZACIÓN
-- ==========================================================
CREATE INDEX IX_Nominas_Fecha ON Nominas(Anio, mes);

-- Nota: Optimizamos Foreign Keys para JOINs y campos de búsqueda frecuente (WHERE).

-- 8.1. Tabla Usuarios: Búsqueda por número de credencial
CREATE INDEX IF NOT EXISTS IX_Usuarios_NoCredencial ON Usuarios(NoCredencial);

-- 8.2. Permisos y Alcances: Optimizamos la resolución de permisos por usuario
CREATE INDEX IF NOT EXISTS IX_PerUsuario_Usuario ON PerUsuario(id_usuario); -- Ya existente en tu script
CREATE INDEX IF NOT EXISTS IX_PerUsuario_Alcance ON PerUsuario(id_talcance);
CREATE INDEX IF NOT EXISTS IX_TipAlcance_Permiso ON TipAlcance(id_tpermiso);

-- 8.3. Tabla Nominas: Búsquedas temporales y filtrado por división/nómina
-- Creamos un índice compuesto para el filtrado jerárquico de periodos
CREATE INDEX IX_Nominas_Fecha ON Nominas(Anio, mes);
CREATE INDEX IF NOT EXISTS IX_Nominas_Periodo ON Nominas(Anio, mes, nomina);
CREATE INDEX IF NOT EXISTS IX_Nominas_Division ON Nominas(id_Division);
CREATE INDEX IF NOT EXISTS IX_Nominas_TipoNom ON Nominas(id_tiponom);

-- 8.4. Tabla UUIDDNom: Tabla de alta cardinalidad
-- Indispensable para conciliación SAT y búsquedas por empleado
CREATE INDEX IF NOT EXISTS IX_UUIDDNom_RFC ON UUIDDNom(RFC); -- Ya existente en tu script
CREATE INDEX IF NOT EXISTS IX_UUIDDNom_UIDD ON UUIDDNom(UIDD);
CREATE INDEX IF NOT EXISTS IX_UUIDDNom_NoCreden ON UUIDDNom(NoCreden);
CREATE INDEX IF NOT EXISTS IX_UUIDDNom_Nomina ON UUIDDNom(id_nomina);

-- 8.5. Tabla Parametros: Optimización para lógica de negocio
-- Generalmente se busca por clave y división activa
CREATE INDEX IF NOT EXISTS IX_Parametros_Busqueda ON Parametros(Clave, id_Division);
CREATE INDEX IF NOT EXISTS IX_Parametros_Vigencia ON Parametros(FechaInic, FechaFin);

-- 8.6. Logs y Estatus SAT: Optimización para auditoría y monitoreo
CREATE INDEX IF NOT EXISTS IX_LogProcesos_Nomina ON LogProcesos(id_nomina);
CREATE INDEX IF NOT EXISTS IX_EstatSATNom_Nomina ON EstatSATNom(id_nomina);
CREATE INDEX IF NOT EXISTS IX_LogProcesos_Fecha ON LogProcesos(Fecha DESC);

-- ==========================================================
-- 9. GENERACIÓN AUTOMÁTICA DE TRIGGERS
-- ==========================================================
DO $$ 
DECLARE
    t text;
BEGIN
    FOR t IN 
        SELECT table_name FROM information_schema.tables 
        WHERE table_schema = 'public' 
        AND table_name IN ('usuarios', 'tipoestasat', 'tiponomina', 'tipodivision', 
                           'tipoestnom', 'tipoparam', 'tipopermiso', 'tipalcance', 
                           'perusuario', 'nominas', 'estatsatnom', 'uuiddnom', 'parametros')
    LOOP
        EXECUTE format('CREATE TRIGGER tr_act_%I BEFORE UPDATE ON %I FOR EACH ROW EXECUTE FUNCTION actualizar_fecha_modificacion()', t, t);
    END LOOP;
END $$;